export const signout = (next) => {
  if (typeof window !== "undefined") {
    localStorage.removeItem("jwt");
    next();
    return fetch(`${API}/signout`, {
      method: "POST",
    })
      .then((response) => {
        // console.log("signout", response);
      })
      .catch((err) => {
        console.log(err);
      });
  }
};

